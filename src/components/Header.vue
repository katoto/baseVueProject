            async getFaucet () {
                // 领取邀请奖励
                if (this.loginSucc && this.loginSucc.tasks.length > 0) {
                    this.showInviteSuccFlag = false
                    let taskDone = await this.$store.dispatch('getTaskDone', this.loginSucc.tasks[0].tid)
